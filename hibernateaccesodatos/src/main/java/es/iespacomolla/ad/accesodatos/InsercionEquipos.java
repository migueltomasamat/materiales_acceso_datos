    private void botonInsertarEquipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonInsertarEquipoActionPerformed
        // TODO add your handling code here:
        Equipo e = new Equipo(modelo.getIdInsertarEquipo(), campoNombreInsercionEquipo.getText(), campoCiudadInsercionEquipo.getText(), comboDivisionInsercionEquipos.getSelectedItem().toString(), comboConferenciasInsercionEquipos.getSelectedItem().toString(), modelo.getEntrenadorPorNombre(jList1.getSelectedValue()), null);
        try {
            modelo.setEquipo(e);
        } catch (DAOEquipoExcepcion ex) {
            JOptionPane.showMessageDialog(this, "Error: No se ha podido almacenar el equipo\n"+ex.getMessage());
        }

        

    }//GEN-LAST:event_botonInsertarEquipoActionPerformed

    private void comboConferenciasInsercionEquiposItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboConferenciasInsercionEquiposItemStateChanged
        // TODO add your handling code here:
        DefaultComboBoxModel modeloCombo = null;
        if(comboConferenciasInsercionEquipos.getSelectedItem().equals("Oeste")){
            modeloCombo = new DefaultComboBoxModel();
            modeloCombo.addAll(modelo.getDivisionesOeste());
            comboDivisionInsercionEquipos.setModel(modeloCombo);
        }else{
            modeloCombo = new DefaultComboBoxModel();
            modeloCombo.addAll(modelo.getDivisionesEste());
            comboDivisionInsercionEquipos.setModel(modeloCombo);
        }
    }//GEN-LAST:event_comboConferenciasInsercionEquiposItemStateChanged

